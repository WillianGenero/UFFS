class Bebida{
	String nome;
	int volume, qntEst, numero;
	double teor, preco;
	public Bebida(String nome, int volume, int qntEst, int numero, double teor, double preco){
		this.nome = nome;
		this.volume = volume;
		this.qntEst = qntEst;
		this.numero = numero;
		this.teor = teor;
		this.preco = preco;
	}
}
